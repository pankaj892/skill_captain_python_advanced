def add_operation(a, b):
    return a + b

def subtract_operation(a, b):
    return a - b

def multiply_operation(a, b):
    return a * b

def divide_operation(a, b):
    return a / b

def modulo_operation(a, b):
    return a % b

def power_operation(a, b):
    return a ** b

def floor_division_operation(a, b):
    return a // b

